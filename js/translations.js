class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('site_lang') || 'en';
        this.toggleBtn = null;
    }

    init() {
        this.applyLanguage(this.currentLang);
        this.setupToggle();
    }

    setupToggle() {
        // Can be called multiple times if DOM changes
        const btn = document.getElementById('lang-toggle');
        if (btn) {
            this.toggleBtn = btn;
            this.updateBtnState();
            btn.onclick = () => this.toggleLanguage();
        }
    }

    toggleLanguage() {
        this.currentLang = this.currentLang === 'en' ? 'ta' : 'en';
        localStorage.setItem('site_lang', this.currentLang);
        this.applyLanguage(this.currentLang);
        this.updateBtnState();
    }

    updateBtnState() {
        if (this.toggleBtn) {
            // Label shows the *other* language you can switch to, or current?
            // Usually shows current. Let's make it toggle-like.
            // If current is EN, show 'à®¤à®®à®¿à®´à¯' to switch. If TA, show 'English'.
            this.toggleBtn.textContent = this.currentLang === 'en' ? 'à®¤à®®à®¿à®´à¯' : 'English';
        }
    }

    applyLanguage(lang) {
        document.documentElement.lang = lang;
        const texts = TRANSLATIONS[lang];

        // 1. Static Text Replacement
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (texts[key]) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = texts[key];
                } else if (el.tagName === 'META') {
                    el.content = texts[key];
                } else if (el.tagName === 'IMG') {
                    el.alt = texts[key];
                } else {
                    el.textContent = texts[key];
                }
            }
        });

        // 2. Custom Events for Page-Specific JS updates (like Product Cards)
        window.dispatchEvent(new CustomEvent('languageChanged', {
            detail: {
                lang,
                texts
            }
        }));
    }

    getText(key) {
        return TRANSLATIONS[this.currentLang][key] || key;
    }
}

const langManager = new LanguageManager();
// Auto-init on load
window.addEventListener('DOMContentLoaded', () => langManager.init());
